-- Lakehouse: RetailLakehouse



CREATE TABLE IF NOT EXISTS DIM_STORE (
    STORE_ID INT,
    STORE_NAME STRING,
    CITY STRING,
    COUNTRY STRING,
    STORE_TYPE STRING
) USING DELTA;

CREATE TABLE IF NOT EXISTS DIM_PRODUCT (
    PRODUCT_ID INT,
    PRODUCT_NAME STRING,
    BRAND STRING,
    DEPARTMENT STRING,
    UNIT_COST DOUBLE
) USING DELTA;

CREATE TABLE IF NOT EXISTS DIM_DATE (
    DATE_KEY DATE,
    YEAR INT,
    QUARTER INT,
    MONTH INT,
    MONTH_NAME STRING,
    DAY_OF_WEEK STRING
) USING DELTA;

CREATE TABLE IF NOT EXISTS FACT_SALES (
    SALE_ID INT,
    STORE_ID INT,
    PRODUCT_ID INT,
    DATE_KEY DATE,
    REVENUE DOUBLE,
    QUANTITY INT,
    COST DOUBLE,
    DISCOUNT_PCT DOUBLE
) USING DELTA;

CREATE TABLE IF NOT EXISTS FACT_INVENTORY (
    PRODUCT_ID INT,
    STORE_ID INT,
    DATE_KEY DATE,
    ON_HAND INT,
    ON_ORDER INT,
    REORDER_POINT INT
) USING DELTA;
